// Take Input From User:
var str = prompt("Please enter a String: ")

// Length Of A String: 
var len = str.length
var msg = "It's a palindrome"

for(var i = 0; i < len/2; i++){
    if(str[i] != str[len -1 -i]){
        console.log(str + " It's not a palindrome")
    }
}
console.log(str + " " + msg)